public class sumOfNumbers {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		int integernum = 5;
		int sumOfIntegers;
		float floatnum = 35.5f;
		// sum of integernum and floatnum(converted to int)	is stored in sumOfIntegers	
		sumOfIntegers = integernum + (int)floatnum;
		System.out.println("The sum of 2 integers is " + sumOfIntegers);

	}

}
